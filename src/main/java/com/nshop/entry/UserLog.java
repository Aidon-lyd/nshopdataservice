package com.nshop.entry;

public class UserLog {

    int user_count;
    int launch_count;
    String bdp_day;

    public int getUser_count() {
        return user_count;
    }

    public void setUser_count(int user_count) {
        this.user_count = user_count;
    }

    public int getLaunch_count() {
        return launch_count;
    }

    public void setLaunch_count(int launch_count) {
        this.launch_count = launch_count;
    }

    public String getBdp_day() {
        return bdp_day;
    }

    public void setBdp_day(String bdp_day) {
        this.bdp_day = bdp_day;
    }
}
